<h3>Selamat Datang di <b>Ujian Online</b></h3>
<br>
<p>Apa itu <b>Ujian Online</b>? </p>
<p><b>Ujian Online</b> adalah proses pelaksanaan ujian yang dilaksanakan dengan sambungan internet melalui media elektronik seperti komputer, laptop, tablet, ataupun smartphone.</p>
<br>
Fungsi <b>Ujian Online</b> </p>
<br>
<b>- Pembelajaran Interaktif</b>
<p>Media pembelajaran ini memudahkan interaksi antara siswa dan materi sehingga memudahkan siswa dalam memahami materi yang disajikan.</p>
<br>
<b>- Evaluasi Pembelajaran</b>
<p>Media ini menyediakan tiga jenis evaluasi yaitu evaluasi materi, evaluasi kuis, dan evaluasi ulangan yang membantu siswa lebih paham .</p>
<br>
<b>- Pembelajaran Jarak Jauh</b>
<p>Media ini memungkinkan siswa dapat belajar pada mata pelajaran tanpa harus secara fisik menghadiri kelas.</p>
<br>
<b>- Fleksibilitas Media</b>
<p>Dengan menggunakan aplikasi <b>Ujian Online</b>, siswa dapat belajar materi pembelajaran kapan saja dan dimana saja.</p>
<br>
<b>- Belajar Tuntas</b>
<p>Media ini didesain menggunakan model pembelajaran tuntas dimana siswa diharapkan menguasai media pembelajaran yang disajikan.</p>
<br>
<b>- Kemudahan Pengembangan</b>
<p>Media ini didesain untuk dapat dikembangkan dengan mudah sesuai kebutuhan siswa mengikuti kurikulum yang berlaku dan teknologi yang terus berkembang.</p>
